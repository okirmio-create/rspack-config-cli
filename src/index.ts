// ── validate ────────────────────────────────────────────────────────────────

program
  .command("validate [file]")
  .description("Validate rspack config structure")
  .action((file = "rspack.config.js") => {
    const filePath = resolve(process.cwd(), file);
    if (!existsSync(filePath)) {
      console.error(chalk.red(`✗ File not found: ${file}`));
      process.exit(1);
    }

    const config = loadConfig(filePath);
    const errors: string[] = [];
    const warnings: string[] = [];

    // Validate entry
    if (!config.entry || Object.keys(config.entry).length === 0) {
      errors.push("entry is missing or empty");
    }

    // Validate output
    if (!config.output) {
      warnings.push("output is not configured (will use defaults)");
    } else {
      if (!config.output.path) warnings.push("output.path is not set");
      if (!config.output.filename) warnings.push("output.filename is not set");
    }

    // Validate module rules
    if (config.module?.rules) {
      config.module.rules.forEach((rule, i) => {
        if (!rule.test) errors.push(`module.rules[${i}] is missing test pattern`);
        if (!rule.use && !rule.type) warnings.push(`module.rules[${i}] has no use or type`);
      });
    }

    // Validate plugins
    if (config.plugins) {
      const supported = ["HtmlRspackPlugin", "CopyRspackPlugin", "DefinePlugin", "MiniCssExtractPlugin"];
      config.plugins.forEach((p) => {
        if (!supported.includes(p.name)) {
          warnings.push(`plugin "${p.name}" is not a known built-in Rspack plugin`);
        }
      });
    }

    // Validate devServer
    if (config.devServer?.port) {
      const port = config.devServer.port;
      if (port < 1 || port > 65535) errors.push(`devServer.port ${port} is out of range (1-65535)`);
    }

    // Summary
    if (errors.length === 0 && warnings.length === 0) {
      console.log(chalk.green(`✔ ${file} is valid`));
      return;
    }

    if (errors.length > 0) {
      console.log(chalk.red(`✗ Validation failed: ${file}`));
      errors.forEach((e) => console.log(chalk.red(`  • ${e}`)));
    }

    if (warnings.length > 0) {
      console.log(chalk.yellow(`⚠ Warnings:`));
      warnings.forEach((w) => console.log(chalk.yellow(`  • ${w}`)));
    }

    if (errors.length > 0) process.exit(1);
  });

// ── show ────────────────────────────────────────────────────────────────────

program
  .command("show [file]")
  .description("Display formatted config summary")
  .action((file = "rspack.config.js") => {
    const filePath = resolve(process.cwd(), file);
    const config = loadConfig(filePath);

    const sep = chalk.dim("─".repeat(50));

    console.log();
    console.log(chalk.bold.cyan(`  📦 Rspack Config: ${file}`));
    console.log(sep);

    // Entry
    console.log(chalk.bold("  Entry:"));
    Object.entries(config.entry ?? {}).forEach(([k, v]) => {
      console.log(`    ${chalk.green(k)} → ${chalk.dim(v)}`);
    });

    // Output
    console.log(chalk.bold("\n  Output:"));
    const out = config.output ?? {};
    console.log(`    path:        ${chalk.dim(String(out.path ?? "(default)"))}`);
    console.log(`    filename:    ${chalk.dim(String(out.filename ?? "(default)"))}`);
    if (out.chunkFilename) console.log(`    chunk:       ${chalk.dim(out.chunkFilename)}`);
    if (out.publicPath) console.log(`    publicPath:  ${chalk.dim(out.publicPath)}`);
    if (out.clean !== undefined) console.log(`    clean:       ${chalk.dim(String(out.clean))}`);

    // Module rules
    const rules = config.module?.rules ?? [];
    console.log(chalk.bold(`\n  Module Rules (${rules.length}):`));
    rules.forEach((r, i) => {
      const useStr = typeof r.use === "string" ? r.use : r.use?.loader ?? r.type ?? "(none)";
      console.log(`    [${i}] test: ${chalk.yellow(r.test)} → ${chalk.green(useStr)}`);
    });

    // Plugins
    const plugins = config.plugins ?? [];
    console.log(chalk.bold(`\n  Plugins (${plugins.length}):`));
    plugins.forEach((p) => {
      console.log(`    • ${chalk.cyan(p.name)}${p.options ? chalk.dim(" " + JSON.stringify(p.options)) : ""}`);
    });

    // Optimization
    const opt = config.optimization ?? {};
    if (Object.keys(opt).length > 0) {
      console.log(chalk.bold("\n  Optimization:"));
      Object.entries(opt).forEach(([k, v]) => {
        console.log(`    ${k}: ${chalk.dim(JSON.stringify(v))}`);
      });
    }

    // DevServer
    const ds = config.devServer ?? {};
    if (Object.keys(ds).length > 0) {
      console.log(chalk.bold("\n  DevServer:"));
      Object.entries(ds).forEach(([k, v]) => {
        console.log(`    ${k}: ${chalk.dim(JSON.stringify(v))}`);
      });
    }

    // Resolve
    const res = config.resolve ?? {};
    if (Object.keys(res).length > 0) {
      console.log(chalk.bold("\n  Resolve:"));
      if (res.extensions) console.log(`    extensions: ${chalk.dim(JSON.stringify(res.extensions))}`);
      if (res.alias) console.log(`    alias: ${chalk.dim(JSON.stringify(res.alias))}`);
      if (res.modules) console.log(`    modules: ${chalk.dim(res.modules)}`);
    }

    console.log(sep);
    console.log();
  });

program.parse(process.argv);
