// ── init ──────────────────────────────────────────────────────────────────

program
  .command("init")
  .description("Generate starter rspack.config.js with entry, output, TypeScript/CSS loaders, HTML plugin")
  .option("-o, --output <path>", "Output path", "rspack.config.js")
  .option("-f, --force", "Overwrite existing file", false)
  .action((opts: { output: string; force: boolean }) => {
    const outPath = resolve(process.cwd(), opts.output);
    if (existsSync(outPath) && !opts.force) {
      console.error(chalk.red(`✗ File already exists: ${opts.output}`));
      console.error(chalk.dim("  Use --force to overwrite."));
      process.exit(1);
    }
    const config = defaultConfig();
    saveConfig(outPath, config);
    console.log(chalk.green(`✔ Created ${opts.output}`));
    console.log();
    console.log(chalk.bold("Next steps:"));
    console.log(`  ${chalk.cyan("npm install -D @rspack/core @rspack/cli")}`);
    console.log(`  ${chalk.cyan("npx rspack serve")}  ${chalk.dim("# dev server")}`);
    console.log(`  ${chalk.cyan("npx rspack build")}  ${chalk.dim("# production build")}`);
    console.log();
    console.log(chalk.dim("Docs: https://rspack.dev/guide/start/quick-start"));
  });

// ── add-loader ─────────────────────────────────────────────────────────────

program
  .command("add-loader <ext>")
  .description("Add a module rule for a file extension")
  .option("-c, --config <file>", "Config file path", "rspack.config.js")
  .option("--test <regex>", "Regex test pattern (e.g. /\\.scss$/)")
  .option("--use <loader>", "Loader name (e.g. sass-loader)")
  .option("--options <json>", "Loader options as JSON")
  .option("--exclude <pattern>", "Exclude pattern (e.g. /node_modules/)")
  .option("--include <pattern>", "Include pattern")
  .option("--type <type>", "Asset module type (e.g. asset/resource)")
  .action(
    (
      ext: string,
      opts: {
        config: string;
        test?: string;
        use?: string;
        options?: string;
        exclude?: string;
        include?: string;
        type?: string;
      }
    ) => {
      const filePath = resolve(process.cwd(), opts.config);
      const config = loadConfig(filePath);
      if (!config.module) config.module = { rules: [] };

      const loaderOptions = opts.options ? JSON.parse(opts.options) as Record<string, unknown> : undefined;
      const testPattern = opts.test ?? `/\\.${ext}$/`;

      const rule: RspackRule = {
        test: testPattern,
        ...(opts.exclude && { exclude: opts.exclude }),
        ...(opts.include && { include: opts.include }),
        ...(opts.use && {
          use: loaderOptions
            ? { loader: opts.use, options: loaderOptions }
            : opts.use,
        }),
        ...(opts.type && { type: opts.type }),
      };

      config.module.rules.push(rule);
      saveConfig(filePath, config);

      console.log(chalk.green(`✔ Added rule for .${ext} files`));
      console.log(chalk.dim(`  test: ${testPattern}`));
      if (opts.use) console.log(chalk.dim(`  use: ${opts.use}`));
      if (opts.type) console.log(chalk.dim(`  type: ${opts.type}`));
    }
  );

// ── add-plugin ─────────────────────────────────────────────────────────────

program
  .command("add-plugin <name>")
  .description("Add a plugin (HtmlRspackPlugin, CopyRspackPlugin, DefinePlugin, MiniCssExtractPlugin)")
  .option("-c, --config <file>", "Config file path", "rspack.config.js")
  .option("--name <name>", "Plugin name")
  .option("--options <json>", "Plugin options as JSON")
  .action(
    (
      name: string,
      opts: { config: string; name?: string; options?: string }
    ) => {
      const filePath = resolve(process.cwd(), opts.config);
      const config = loadConfig(filePath);
      if (!config.plugins) config.plugins = [];

      const pluginName = opts.name ?? name;
      const pluginOptions = opts.options ? JSON.parse(opts.options) as Record<string, unknown> : undefined;

      const supported = ["HtmlRspackPlugin", "CopyRspackPlugin", "DefinePlugin", "MiniCssExtractPlugin"];
      if (!supported.includes(pluginName)) {
        console.error(chalk.yellow(`⚠ Unknown plugin: ${pluginName}`));
        console.error(chalk.dim(`  Supported: ${supported.join(", ")}`));
      }

      config.plugins.push({ name: pluginName, options: pluginOptions });
      saveConfig(filePath, config);

      console.log(chalk.green(`✔ Added plugin: ${pluginName}`));
      if (pluginOptions) console.log(chalk.dim(`  options: ${JSON.stringify(pluginOptions)}`));
    }
  );

// ── set-optimization ────────────────────────────────────────────────────────

program
  .command("set-optimization")
  .description("Configure optimization settings")
  .option("-c, --config <file>", "Config file path", "rspack.config.js")
  .option("--minimize <bool>", "Enable minimization (true/false)")
  .option("--split-chunks <bool>", "Enable splitChunks (true/false)")
  .option("--chunk-ids <mode>", "Chunk IDs mode: named, deterministic")
  .option("--module-ids <mode>", "Module IDs mode: named, deterministic, hashed")
  .option("--tree-shaking <bool>", "Enable tree shaking (true/false)")
  .option("--side-effects <bool>", "Respect sideEffects flag (true/false)")
  .action(
    (opts: {
      config: string;
      minimize?: string;
      splitChunks?: string;
      chunkIds?: string;
      moduleIds?: string;
      treeshaking?: string;
      sideEffects?: string;
    }) => {
      const filePath = resolve(process.cwd(), opts.config);
      const config = loadConfig(filePath);
      if (!config.optimization) config.optimization = {};

      const parseBool = (v: string | undefined): boolean | undefined =>
        v === "true" ? true : v === "false" ? false : undefined;

      if (opts.minimize !== undefined) config.optimization.minimize = parseBool(opts.minimize);
      if (opts.splitChunks !== undefined) config.optimization.splitChunks = parseBool(opts.splitChunks);
      if (opts.chunkIds) config.optimization.chunkIds = opts.chunkIds;
      if (opts.moduleIds) config.optimization.moduleIds = opts.moduleIds;
      if (opts.treeshaking !== undefined) config.optimization.treeshaking = parseBool(opts.treeshaking);
      if (opts.sideEffects !== undefined) config.optimization.sideEffects = parseBool(opts.sideEffects);

      saveConfig(filePath, config);
      console.log(chalk.green("✔ Optimization updated"));
      console.log(chalk.dim(`  ${JSON.stringify(config.optimization, null, 2)}`));
    }
  );

