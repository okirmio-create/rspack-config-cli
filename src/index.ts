import { program } from "commander";
import chalk from "chalk";
import { writeFileSync, readFileSync, existsSync } from "fs";
import { resolve } from "path";

// ─── Types ─────────────────────────────────────────────────────────────────

interface RspackRule {
  test: string;
  use?: string | { loader: string; options?: Record<string, unknown> };
  type?: string;
  exclude?: string;
  include?: string;
}

interface RspackPlugin {
  name: string;
  options?: Record<string, unknown>;
}

interface RspackOptimization {
  minimize?: boolean;
  splitChunks?: boolean;
  chunkIds?: string;
  moduleIds?: string;
  treeshaking?: boolean;
  sideEffects?: boolean;
}

interface RspackDevServer {
  port?: number;
  host?: string;
  hot?: boolean;
  open?: boolean;
  proxy?: Record<string, unknown>;
  historyApiFallback?: boolean;
  compress?: boolean;
}

interface RspackOutput {
  path?: string;
  filename?: string;
  chunkFilename?: string;
  publicPath?: string;
  clean?: boolean;
  libraryType?: string;
}

interface RspackResolve {
  extensions?: string;
  alias?: Record<string, unknown>;
  modules?: string;
  mainFields?: string;
}

interface RspackConfig {
  entry?: Record<string, string>;
  output?: Partial<RspackOutput>;
  module?: { rules: RspackRule[] };
  plugins?: RspackPlugin[];
  optimization?: Partial<RspackOptimization>;
  devServer?: Partial<RspackDevServer>;
  resolve?: Partial<RspackResolve>;
  [key: string]: unknown;
}

// ─── Helpers ───────────────────────────────────────────────────────────────

function loadConfig(filePath: string): RspackConfig {
  if (!existsSync(filePath)) {
    console.error(chalk.red(`✗ Config file not found: ${filePath}`));
    process.exit(1);
  }
  const raw = readFileSync(filePath, "utf8");
  // Extract JSON from JS module export for simple manipulation
  const match = raw.match(/\/\*\s*__RSPACK_CONFIG_JSON__\s*([\s\S]+?)\s*__END__\s*\*\//);
  if (match) {
    return JSON.parse(match[1].trim()) as RspackConfig;
  }
  console.error(chalk.red(`✗ Cannot parse config (must be generated by this CLI)`));
  process.exit(1);
}

function saveConfig(filePath: string, config: RspackConfig): void {
  const json = JSON.stringify(config, null, 2);
  const js = generateJsFromConfig(config, json);
  writeFileSync(filePath, js, "utf8");
}

function generateJsFromConfig(config: RspackConfig, json: string): string {
  const pluginLines = (config.plugins ?? []).map((p) => {
    const opts = p.options ? `, ${JSON.stringify(p.options)}` : "";
    const known: Record<string, string> = {
      HtmlRspackPlugin: `new rspack.HtmlRspackPlugin(${opts ? JSON.stringify(p.options, null, 4) : "{}"})`,
      CopyRspackPlugin: `new rspack.CopyRspackPlugin(${opts ? JSON.stringify(p.options, null, 4) : "{ patterns: [] }"})`,
      DefinePlugin: `new rspack.DefinePlugin(${p.options ? JSON.stringify(p.options, null, 4) : "{}"})`,
      MiniCssExtractPlugin: `new rspack.CssExtractRspackPlugin(${p.options ? JSON.stringify(p.options, null, 4) : "{}"})`,
    };
    return `    ${known[p.name] ?? `/* ${p.name} - configure manually */`}`;
  });

  const ruleLines = (config.module?.rules ?? []).map((r) => {
    const useStr =
      typeof r.use === "string"
        ? `"${r.use}"`
        : r.use
        ? JSON.stringify(r.use, null, 6)
        : "undefined";
    return `    {
      test: ${r.test},${r.exclude ? `\n      exclude: ${r.exclude},` : ""}${r.include ? `\n      include: ${r.include},` : ""}
      use: ${useStr},${r.type ? `\n      type: "${r.type}",` : ""}
    }`;
  });

  return `/* __RSPACK_CONFIG_JSON__
${json}
__END__ */
import { rspack } from "@rspack/core";

export default {
  entry: ${JSON.stringify(config.entry ?? { main: "./src/index.ts" }, null, 4)},

  output: ${JSON.stringify(config.output ?? {}, null, 4)},

  module: {
    rules: [
${ruleLines.join(",\n")}
    ],
  },

  plugins: [
${pluginLines.join(",\n")}
  ],

  optimization: ${JSON.stringify(config.optimization ?? {}, null, 4)},

  devServer: ${JSON.stringify(config.devServer ?? {}, null, 4)},

  resolve: ${JSON.stringify(config.resolve ?? {}, null, 4)},
};
`;
}

// ─── Default config ────────────────────────────────────────────────────────

function defaultConfig(): RspackConfig {
  return {
    entry: { main: "./src/index.ts" },
    output: {
      filename: "[name].[contenthash].js",
      chunkFilename: "[name].[contenthash].chunk.js",
      path: "./dist",
      clean: true,
      publicPath: "auto",
    },
    module: {
      rules: [
        {
          test: "/\\.(ts|tsx|js|jsx)$/",
          use: {
            loader: "builtin:swc-loader",
            options: {
              jsc: { parser: { syntax: "typescript", tsx: true } },
            },
          },
          exclude: "/node_modules/",
        },
        {
          test: "/\\.css$/",
          use: "css-loader",
          type: "javascript/auto",
        },
      ],
    },
    plugins: [
      { name: "HtmlRspackPlugin", options: { template: "./public/index.html" } },
    ],
    optimization: {},
    devServer: {
      port: 3000,
      hot: true,
    },
    resolve: {
      extensions: [".ts", ".tsx", ".js", ".jsx", ".json"] as unknown as string,
    },
  };
}

